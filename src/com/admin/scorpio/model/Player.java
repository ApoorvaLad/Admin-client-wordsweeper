package com.admin.scorpio.model;

/**
 * Object model for Player
 * 
 * @author Rishitha
 *
 */

public class Player {

	private String name;
	private String score;

	public String getname() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

}
